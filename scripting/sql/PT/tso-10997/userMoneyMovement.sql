SELECT MOVEMENTID, USERINTID, HEARTBEAT, MOVEMENTTYPE, REFERENCEID, AMOUNT, TCHIPS, MOVEMENTTIME, TRANSTYPE, DBMID    --marketdbm
FROM PTMONEYMOVEMENTS --
WHERE userintid in (135663282)
